import Image from "next/image";
import Link from "next/link";

import ActionDropdown from "@/components/ActionDropdown";
import { Chart } from "@/components/Chart";
import { FormattedDateTime } from "@/components/FormattedDateTime";
import { Thumbnail } from "@/components/Thumbnail";
import { Separator } from "@/components/ui/separator";

import { getFiles, getTotalSpaceUsed } from "@/lib/actions/file.actions";
import { constructDownloadUrl, convertFileSize, getUsageSummary } from "@/lib/utils";

const Dashboard = async () => {
  const [files, totalSpace] = await Promise.all([
    getFiles({}),
    getTotalSpaceUsed(),
  ]);

  const usageSummary = getUsageSummary(files);

  return (
    <div className="dashboard-container">
      <section>
        <Chart used={totalSpace?.used ?? 0} />


        <ul className="dashboard-summary-list">
          {usageSummary.map((summary) => (
            <Link
              href={summary.url}
              key={summary.title}
              className="dashboard-summary-card"
            >
              <div className="space-y-4">
                <div className="flex justify-between gap-3">
                  <Image
                    src={summary.icon}
                    width={100}
                    height={100}
                    alt="uploaded image"
                    className="summary-type-icon"
                  />
                  <h4 className="summary-type-size">
                    {convertFileSize(summary.size ?? 0)}
                  </h4>
                </div>

                <h5 className="summary-type-title">{summary.title}</h5>
                <Separator className="bg-light-400" />
                <FormattedDateTime
                  date={summary.latestDate}
                  className="text-center"
                />
              </div>
            </Link>
          ))}
        </ul>
      </section>

      <section className="dashboard-recent-files">
        <h2 className="h3 xl:h2 text-light-100">Recent files uploaded</h2>

        {files.length > 0 ? (
  <ul className="mt-5 flex flex-col gap-5">
    {files.slice(0, 5).map((file: any) => (
      <Link
        key={file.id}
        href={constructDownloadUrl(file.path)}
        target="_blank"
        className="flex items-center gap-3"
      >
        <Thumbnail
          type={file.type}
          extension={file.extension}
          url={constructDownloadUrl(file.path)}
        />

        <div className="recent-file-details">
          <p className="recent-file-name">{file.name}</p>
        </div>
      </Link>
    ))}
  </ul>
) : (
  <p className="empty-list">No files uploaded</p>
)}

      </section>
    </div>
  );
};

export default Dashboard;
